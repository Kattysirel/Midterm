def texto_a_matriz(texto):
    b_texto = [ord(c) for c in texto]
    matriz = [b_texto[i:i+4] for i in range(0,16,4)]
    return matriz

def addRoundKey(m_texto, clave):
    nuevaMatriz = []
    for i in range(4):
        n_fila = []
        for j in range(4):
            cifr = (m_texto[i][j] ^ clave[i][j])
            n_fila.append(cifr)
        nuevaMatriz.append(n_fila)    
    return nuevaMatriz

