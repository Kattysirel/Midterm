def convertir_a_binario(matriz_hex):
    matriz_bin = []
    for i in range(len(matriz_hex)):
        fila_bin = []
        for val in matriz_hex[i]:
            x = bin(val)[2:]
            rellenar_bits = x.zfill(8)
            fila_bin.append(rellenar_bits)
        matriz_bin.append(fila_bin)
    return matriz_bin

def substituir_subbytes(matriz_bin, s_box):
    matriz_cifrada = []
    for i in range(len(matriz_bin)):
        fila_cifrada = []
        for byte_binario in matriz_bin[i]:
            x_bin = byte_binario[0:4]
            y_bin = byte_binario[4:8]
            x_decimal = int(x_bin, 2)
            y_decimal = int(y_bin, 2)
            valor_sustitucion = s_box[x_decimal][y_decimal]           
            fila_cifrada.append(valor_sustitucion)         
        matriz_cifrada.append(fila_cifrada)    
    return matriz_cifrada

def Shift_Rows(matriz_S):
    matriz_Actualizada = []
    for i in range(4):
        fila_nueva = []
        for j in range(4):
            fila_nueva.append(matriz_S[i][(j + i) % 4 ])
        matriz_Actualizada.append(fila_nueva)
    return matriz_Actualizada

def mix_columnas(matriz):
    mix=[
        [2,2,1,1],
        [1,2,3,1],
        [1,1,2,3],
        [3,1,1,3],
    ]
    new_matriz=[[0 for _ in range (4)] for _ in range(4)]
    for f in range (4):
        for c in range(4):
            suma=0
            for i in range(4):
                suma += mix[f][i]*matriz[i][c]
                new_matriz [f][c]=suma
    return new_matriz

